        // GET: Service
        public ActionResult Index()
        {
            var services = new Collection<Service>();

            string text = "get-service";
            using (PowerShell PowerShellInstance = PowerShell.Create())
            {
                // use "AddScript" to add the contents of a script file to the end of the execution pipeline.
                // use "AddCommand" to add individual commands/cmdlets to the end of the execution pipeline.
                PowerShellInstance.AddScript(text);

                var PSOutput = PowerShellInstance.Invoke();
                foreach (var outputItem in PSOutput)
                {
                    // if null object was dumped to the pipeline during the script then a null
                    // object may be present here. check for null to prevent potential NRE.
                    if (outputItem != null)
                    {
                        var service = new Service
                        {
                            DisplayName = outputItem.Members["DisplayName"].Value.ToString(),
                            Status = outputItem.Members["Status"].Value.ToString(),
                            Name = outputItem.Members["Name"].Value.ToString()
                        };
                        services.Add(service);
                    }
                }
            }
            return View(services);
        }

